def normalize_language(value: object) -> str:
    language = str(value or DEFAULT_LANGUAGE).lower()
    return language if language in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def catalog(language: object) -> dict[str, str]:
    return TRANSLATIONS[normalize_language(language)]


def frontend_catalog(language: object) -> dict[str, str]:
    allowed_prefixes = ("common.", "download.", "index.", "jobs.", "js.", "job.", "preview.", "theme.")
    allowed_keys = {"nav.jobs", "common.open_log", "result.select_all", "result.unselect_all"}
    return {
        key: value
        for key, value in catalog(language).items()
        if key in allowed_keys or key.startswith(allowed_prefixes)
    }


def translate(language: object, key: str, **values: Any) -> str:
    language_code = normalize_language(language)
    text = TRANSLATIONS[language_code].get(key) or TRANSLATIONS[DEFAULT_LANGUAGE].get(key) or key
    return text.format(**values) if values else text


def status_labels(language: object) -> dict[str, str]:
    language_code = normalize_language(language)
    return STATUS_LABELS.get(language_code, STATUS_LABELS[DEFAULT_LANGUAGE])


def download_type_labels(language: object) -> dict[str, str]:
    language_code = normalize_language(language)
    return DOWNLOAD_TYPE_LABELS.get(language_code, DOWNLOAD_TYPE_LABELS[DEFAULT_LANGUAGE])


def localize_job(job: dict[str, Any], language: object) -> dict[str, Any]:
    labels = status_labels(language)
    job["status_label"] = labels.get(str(job.get("status") or ""), str(job.get("status") or ""))
    return job
